# Program to perform different Logical operators operations on numbers

a = 10
b = 20
c = 10

print("a =", a, ", b =", b, ", c =", c)

# Logical AND: True if both conditions are True
print("a == 10 and b == 20:", a == 10 and b == 20)

# Logical OR: True if at least one condition is True
print("a == 10 or b == 15:", a == 10 or b == 15)

# Logical NOT: Negates the condition
print("not (a == b):", not (a == b))

# More examples
print("a == c and b > a:", a == c and b > a)
print("a != c or b < a:", a != c or b < a)
