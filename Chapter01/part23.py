import math

# math.ceil(x): Round x up to the nearest integer
print("math.ceil(10.23) =", math.ceil(10.23))  # Output: 11

# math.floor(x): Round x down to the nearest integer
print("math.floor(18.9) =", math.floor(18.9))  # Output: 18

# math.exp(x): Returns e raised to the power x (e^x)
print("math.exp(1) =", math.exp(1))  # Output: 2.718281828459045

# math.log(x): Natural logarithm (base e) of x
print("math.log(2.71828) =", math.log(2.71828))  # Output: approx 1

# math.log(x, base): Logarithm of x with specified base
print("math.log(8, 2) =", math.log(8, 2))  # Output: 3.0

# math.sqrt(x): Square root of x
print("math.sqrt(16) =", math.sqrt(16))  # Output: 4.0

# abs(x): Absolute value of x
print("abs(-2) =", abs(-2))  # Output: 2
print("abs(4) =", abs(4))    # Output: 4

# max(x1, x2, ...): Returns the largest value
print("max(10, 20, 30, 40) =", max(10, 20, 30, 40))  # Output: 40

# min(x1, x2, ...): Returns the smallest value
print("min(10, 20, 30, 40) =", min(10, 20, 30, 40))  # Output: 10

# pow(x, y): Returns x raised to the power y (x^y)
print("pow(2, 3) =", pow(2, 3))  # Output: 8

# round(x): Rounds x to the nearest integer
print("round(3.6) =", round(3.6))  # Output: 4
print("round(3.4) =", round(3.4))  # Output: 3
