# This code demonstrates the usage of if statements in Python with user input

# Example 1: Check if a number is positive
num1 = int(input("Enter a number: "))
if num1 > 0:
    print("The number is positive.")


# Example 2: Check if a person is eligible to vote
age = int(input("Enter your age: "))
if age >= 18:
    print("You are eligible to vote.")


# Example 3: Check if a string contains the word 'python'
text = input("Enter a sentence: ")
if 'python' in text.lower():
    print("The sentence contains the word 'python'.")

